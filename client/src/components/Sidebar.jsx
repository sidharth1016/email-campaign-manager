const navItems = [
  { path: '/dashboard', label: 'Dashboard', icon: 'dashboard' },
  { path: '/campaigns', label: 'Campaigns', icon: 'campaigns' },
  { path: '/leads', label: 'Leads', icon: 'leads' },
  { path: '/templates', label: 'Templates', icon: 'templates' },
  { path: '/analytics', label: 'Analytics', icon: 'analytics' },
];

export default function Sidebar() {
  const { user, logout } = useAuth();
  const location = useLocation();
  const navigate = useNavigate();

  const handleLogout = () => {
    logout();
    navigate('/login');
  };

  return (
    <aside className="sidebar">
      <div className="sidebar-logo">
        <div className="logo-icon">✉</div>
        <span>InboxKit</span>
      </div>

      <nav className="sidebar-nav">
        <div className="nav-section-label">Main Menu</div>
        {navItems.map(item => (
          <div
            key={item.path}
            className={`nav-item ${location.pathname === item.path ? 'active' : ''}`}
            onClick={() => navigate(item.path)}
          >
            {icons[item.icon]}
            {item.label}
          </div>
        ))}
      </nav>

      <div className="sidebar-footer">
        <div className="user-info" onClick={handleLogout} title="Logout">
          <div className="user-avatar">{user?.name?.[0]?.toUpperCase()}</div>
          <div className="user-details">
            <div className="user-name">{user?.name}</div>
            <div className="user-role">Click to logout</div>
          </div>
          <span style={{ color: 'var(--text-muted)', fontSize: 14 }}>→</span>
        </div>
      </div>
    </aside>
  );
}
